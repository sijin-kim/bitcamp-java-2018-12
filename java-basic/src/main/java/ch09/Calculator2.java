package ch09;

public class Calculator2 {

  static int result =0; //클래스 필드(스태틱 필드)
  
  static void plus(int a) {
   result +=a;
  
}
  static void minus(int a ) {
    result -=a;
  
}
  static void multiple(int a ) {
    result *=a;
  
}
  static void divide(int a ) {
    result /=a;
    
  }

}

   
   



